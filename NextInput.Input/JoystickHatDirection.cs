namespace NextInput.Input;

/// <summary>
/// Represents a joystick hat with 8 directions
/// </summary>
public enum JoystickHatDirection
{
    /// <summary>
    /// The centered position (when nothing is pressed)
    /// </summary>
    Centered,
    /// <summary>
    /// The up direction
    /// </summary>
    Up,
    /// <summary>
    /// The right direction
    /// </summary>
    Right,
    /// <summary>
    /// The down direction
    /// </summary>
    Down,
    /// <summary>
    /// The left direction
    /// </summary>
    Left,
    /// <summary>
    /// The right and up direction
    /// </summary>
    RightUp,
    /// <summary>
    /// The right and down direction
    /// </summary>
    RightDown,
    /// <summary>
    /// The left and up direction
    /// </summary>
    LeftUp,
    /// <summary>
    /// The left and down direction
    /// </summary>
    LeftDown
}